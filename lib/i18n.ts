export type Language = 'cs' | 'en'

export function getLanguageFromHost(host: string): Language {
  const domain = host.split(':')[0]
  if (domain.endsWith('.ai') || domain.endsWith('.au') || domain.endsWith('.com')) return 'en'
  return 'cs'
}

